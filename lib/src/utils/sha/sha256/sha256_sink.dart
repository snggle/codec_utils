import 'dart:typed_data';

import 'package:codec_utils/src/utils/sha/hash/digest.dart';
import 'package:codec_utils/src/utils/sha/sha256/a_sha_32bit_sink.dart';

/// [Sha256Sink] initializes the hash state. This class processes input  and applies SHA-256 transformations to compute the final digest.
class Sha256Sink extends ASha32BitSink {
  Sha256Sink(Sink<Digest> sink)
      : super(
          sink,
          Uint32List.fromList(<int>[
            0x6a09e667,
            0xbb67ae85,
            0x3c6ef372,
            0xa54ff53a,
            0x510e527f,
            0x9b05688c,
            0x1f83d9ab,
            0x5be0cd19,
          ]),
        );
}
